मार्गरेट एलिजाबेथ नोबल या सिस्टर निवेदिता के नाम से विख्यात स्वामी विवेकानंद की शिष्या ने अपना सम्पूर्ण जीवन भारत और उसके नागरिकों की सेवा के लिए समर्पित कर दिया था। अंग्रेज-आयरिश इस सन्यासिन ने भारत की संस्कृति और हिन्दू धर्म को अपनाकर स्वामी विवेकानंद के आह्वान पर भारत भूमि को अपना कर्म क्षेत्र बनाया और वेदांत के अंतिम लक्ष्य को प्राप्त करने की प्रेरणा दी। भारत के लिए कार्य करने वाले महान व्यक्तियों में सिस्टर निवेदिता का नाम श्रेष्ठ लोगों में आता है। निवेदिता ने भारत में लड़कियों की शिक्षा के लिए महान कार्य किया है। इसके साथ ही भारत के स्वतन्त्रता आंदोलन को भी निवेदिता ने प्रत्यक्ष रूप से बढ़ावा दिया था।
