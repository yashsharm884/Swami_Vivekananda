सिस्टर निवेदिता का जन्म 28 अक्तूबर 1867 में उत्तरी आयरलैंड के डंगनॉन में हुआ था। सिस्टर निवेदिता मैरी और सैमुएल रिचमंड नोबल की सबसे बड़ी संतान थी। उनके पिता आयरलैंड के एक चर्च के पुजारी थे। किन्तु मार्गरेट जब 10 साल की थीं तभी उनके पिता का देहांत हो गया था। उनके पिता ने बचपन में ही उन्हें मानव सेवा का पाठ पढ़ाया था, और ये कहा था कि मानव सेवा ही सच्ची ईश्वर भक्ति है। उनके पिता के देहांत के बाद उनकी नानी हैमिल्टन ने उसकी देखभाल की थी। उसकी नानी आयरलैंड स्वाधीनता आंदोलन कि एक प्रमुख नेता रह चुकी थीं। सिस्टर निवेदिता के अंदर बचपन से ही धार्मिक प्रवत्ति थी जिसके कारण वे चर्च के कार्यों और गतिविधियों में भाग लिया करती थीं। 17 वर्ष की आयु में उन्होने आयरलैंड और इंग्लैंड के अलग –अलग स्कूलों में अध्यापन का कार्य किया। 1892 मे उन्होने विम्बलडन नामक जगह पर अपने स्वयं के स्कूल की स्थापना की। एक अच्छी लेखक और वक्ता निवेदिता लंदन के सीसेम क्लब की सदस्य थीं जहां उनकी मुलाक़ात जॉर्ज बर्नार्ड शॉ और थॉमस हक्सले जैसे प्रसिद्ध लेखकों से हुई । 



<h2 align="center">स्वामी विवेकानंद से ऐतिहासिक भेंट -</h2>

<br>
<br>
<p align="center">
<img src="images/sister6.jpg" align="center" width="50%"><br><br>
