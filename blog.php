1906 के बंगाल के सूखे के दौरान लोगों का उपचार करते समय सिस्टर निवेदिता मलेरिया से संक्रमित हो गईं। जिसने अंततोगत्वा उनका जीवन ले लिया। 44 वर्ष की आयु में दार्जिलिंग में 13 अक्टूबर 1911 को सिस्टर निवेदिता का देहांत हुआ। उनके निधन पर समूचे भारत सहित पूरे विश्व में करुणा और शोक की लहर फैल गयी। भारत की वह बेटी जिसने इंग्लैंड में जन्म लेने के बावजूद भारत को अपना समझा और इस देश के लोगों की सेवा करते हुए ही अपने प्राणो की बली दे दी। वह हम सब के लिए एक प्रेरणा स्वरूप हैं। निवेदिता द्वारा 1897 और 1911 के बीच लिखे गए पत्रों से उनके भारत और भारत में उनके कार्यों संबंधी विचारों का पता चलता है। सिस्टर निवेदिता के विचारों में भारत के लिए एक विशेष प्रकार का आग्रह था जो बहुत कम भारतीयों में देखने को मिलता है। सिस्टर निवेदिता की मृत्यु के पश्चात रामकृष्ण मिशन के श्री गोनेन महाराज ने उनकी चीता को अग्नि दी थी तथा उनका हिन्दू रीति-रिवाजों के साथ अंतिम संस्कार किया गया।  उनकी समाधि पर पड़े शिलालेख पर अंकित है – “यहाँ रामकृष्ण विवेकानंद की सिस्टर निवेदिता विश्राम में हैं, जिंहोने भारत को अपना सर्वस्व दे दिया।“

