<p align="center" style="margin-left: 50px;
    margin-right: 50px;
    font-size: 30px;
    padding: 10px;">

स्वामी विवेकानंद से एलिज़ाबेथ नोबल की मुलाक़ात 1895 में इंग्लैंड में हुई थी।स्वामी विवेकानंद के तेजस्वी व्यक्तित्व से सिस्टर निवेदिता इतनी प्रभावित हुई की उनके विचार वेदान्त और आध्यात्मिक अनुभूति की और प्रवृत्त हो गयी। स्वामी विवेकानंद ने अपने भाषण में कहा था, मुझे सभी से आशा नहीं है,मुझे केवल चुने हुए बीस लोग चाहिए जो अपना सम्पूर्ण जीवन निष्ठा एवं ईमानदारी से मानव सेवा में न्योछावर कर सकें। उनके इस असाधारण आग्रह से मार्गरेट नोबल इतनी प्रभावित हुई कि वे अगले ही दिन सुबह स्वामी विवेकानंद के पास पहुच गईं। स्वामी विवेकानंद से सिस्टर निवेदिता ने कहा कि आपने कल बीस लोगो के बारे में बात की थी, उन्नीस लोगों का तो पता नहीं पर एक मैं आपके कार्य के लिए प्रस्तुत हूँ।
<br><br>

 भारत में स्त्रियों की अत्यंत दयनीय स्थिति के कारण स्वामी विवेकानंद उन्हें शिक्षा द्वारा जागरूक कर उनके अंदर के आत्म विश्वास को पुनः लौटाना चाहते थे। इस कार्य के लिए स्वामी जी एक ऐसे व्यक्ति की खोज में थे, जो सदियों से दयनीय स्थिति में पड़ी भारतीय नारी के गौरव को फिर से लौटा सके क्योंकि आधी आबादी को बिना शिक्षित किए कोई भी समाज अपने आप को सभ्य समाज नहीं कह सकता। स्वामी जी ने सिस्टर निवेदिता से कहा था, कि उन्हें एक स्त्री नहीं बल्कि एक शेरनी की आवश्यकता है जो उनके कलकत्ते के कार्य को कर सके। स्वामी विवेकानंद को मार्गरेट नोबल में एक आदर्श नारी के सभी गुण विद्यमान दिखाई दिये जिसके कारण स्वामी विवेकानंद ने उन्हे भारत आकर कार्य करने का न्यौता दिया। मार्गरेट नोबल पहली बार 1896 में भारत आयीं और स्वामी विवेकानंद के द्वारा निर्देशित किए गए कार्यों में सहयोग देने लगीं।

<br><br>
स्वामी विवेकानंद ने मार्गरेट नोबल को 25 मार्च 1898 को पूर्ण ब्रह्मचर्य कि शपथ दिलाते हुए दीक्षा प्रदान की , और उनका नाम बदलकर निवेदिता रख दिया। स्वामी जी ने उन्हे सन्यास का धर्म ग्रहण कराते हुए यह आज्ञा दी कि –“जाओ और उसका अनुसरण करो, जिसने बुद्ध कि दृष्टि प्राप्त करने से पहले 500 बार जन्म लिया और अपने सभी जीवन लोक कल्याण के लिए समर्पित कर दिये। दीक्षा के समय स्वामी विवेकन्द ने मार्गरेट नोबल का नाम बदलकर उन्हें नया नाम निवेदिता दिया, जो पूरे विश्व में प्रसिद्ध हुआ। सिस्टर निवेदिता ने स्वामी विवेकानंद के साथ भारत का भ्रमण किया था। जिसके बाद वो कलकत्ता में बस गईं । जहां उन्होने लड़कियों के लिए स्कूल खोला। भगिनी निवेदिता ने भारत भूमि को अपना कार्य क्षेत्र बनाया था। इतना ही नहीं उन्होने भारतीय जनमानस की सेवा के लिए अपनी मातृभूमि आयरलैंड का भी त्याग कर दिया था। सिस्टर निवेदिता ने श्री अरविंद के बाद वंदे मातरम का सम्पादन भी किया था जो अपने राष्ट्र भक्ति और स्वतन्त्रता संग्राम को बढ़ावा देने के लिए जानी जाती थी। सिस्टर निवेदिता ने अपने लेखों और पुस्तकों द्वारा भारतीय जनता पर हो रहे अत्याचारों का पुरजोर खंडन किया था। 

<br><br>
<h2 align="center">
सिस्टर निवेदिता और भारतीय कला -
</h2>

<br>
<br>
<p align="center">
<img src="images/sister4.jpg" align="center"><br><br>
</p>



<p align="center" style="margin-left: 50px;
    margin-right: 50px;
    font-size: 30px;
    padding: 10px;">

एक बहू आयामी व्यक्तित्व वाली सिस्टर निवेदिता भारत में लड़कियों की शिक्षा और सामाजिक कार्यों तक ही सीमित नहीं थीं। सिस्टर निवेदिता ने भारतीय कला की परंपराओं को पुनर्जीवित करने का भी प्रयास किया है। निवेदिता के अंदर आरंभ से ही काला के प्रति एक तीक्ष्ण आलोचनात्मक आग्रह था । सिस्टर निवेदिता ने यूरोप के प्रसिद्ध म्यूज़ियमों में गहन अध्ययन किया था। उन्होने भारत की कई प्राचीन स्मारकों का भी भ्रमण किया था जिनमें सांची, बोध गया, अजंता एलोरा, उदयगीरी-खंडगीरी तथा सारनाथ इत्यादि प्रमुख हैं। उनके इस यूरोपीय और भारतीय कला के ऐतिहासिक इमारतों के भ्रमण ने उनके अंदर पाश्चात्य और प्राच्य कला परंपराओं के विषय में उच्च स्तरीय ज्ञान भर दिया था। स्वामी विवेकानंद की तरह ही सिस्टर निवेदिता ने अमेरिका मे भारतीय कला के ऊपर व्याख्यान दिये थे। इन व्याख्यानों में उन्होने भी स्वामी विवेकानंद की भांति इस तथ्य को नकार दिया था की यूनानी कला ने भारतीय कला को प्रभावित किया है तथा भारतीय कला ने यूनानी कला से प्रेरणा ली है। 
<br><br>
सिस्टर निवेदिता ने पाश्चात्य विद्वानों के इस तथ्य को भी नकार दिया कि यूनानी कला से पहले भारतीय कला का कोई अस्तित्व नहीं था। स्वामी विवेकानंद ने भी  अपने पेरिस के 1990 के दौरे में इस भ्रांति को नकार दिया था। भारत में किए गए अपने कला संबंधी अध्ययनों और प्राप्त हुए अनुभवों के आधार पर सिस्टर निवेदिता का यह पूर्ण विश्वास था की भारतीय कला को पुनः जागरूकता की आवश्यकता है।  यह जागरूकता केवल अपने प्राचीन इतिहास में जाने पर ही प्राप्त होगी क्योंकि वर्तमान की भारतीय कला अपने ऐतिहासिक रूप से संबंध तोड़ चुकी थी। सिस्टर निवेदिता ने यह भी अनुभव किया की भारतीय कला की शिक्षा भी पाश्चात्य दृष्टिकोण के अनुसार ही दी जा रही थी। निवेदिता ने भारतीय कला को भारतीय प्राचीन कला के दृष्टिकोण से सिखाये जाने का समर्थन किया । इसके अलावा उन्होने कला को राष्ट्रीय जागरण का एक सशक्त माध्यम माना।
<br>
<br>
जापान के प्रसिद्ध कला आलोचक कोकाजू ओकाकुरा के साथ विचार विमर्श के बाद निवेदिता के भारतीय कला संबंधी विचारों की और अधिक बल मिला।  निवेदिता ने रवीन्द्रनाथ के भतीजे अबनीन्द्रनाथ टैगोर के साथ भी कला संबंधी परस्पर विचार विमर्श किया था। निवेदिता और हैवल के द्वारा भारतीय कला संबंधी दिये गए दृष्टिकोण का प्रभाव अबनीन्द्रनाथ टैगोर पर पड़ा था। इन कला प्रेमियों द्वारा 1907 में “इंडियन सोसायटी फॉर ओरियंटल आर्ट” की स्थापना की गयी। इनके द्वारा जिस कला के रूप की अभिव्यक्ति हुई उसे कला के “बंगाल स्कूल” के रूप में जाना जाता है। निवेदिता ने प्रख्यात चित्रकारों नंदलाल बोस, आसिफ हलदार और सुरेन्द्र गांगुली को भी प्रेरित किया था। सिस्टर निवेदिता एक कुशल लेखक भी थीं। जिन्होने अपने उच्च कोटी के लेखों द्वारा भारतीय कला के विभिन्न पक्षो पर प्रकाश डाला है। उनका मानना था कि एक साधारण मनुष्य तक पहुँचने के लिए कला से बेहतर कोई माध्यम नहीं है। 
