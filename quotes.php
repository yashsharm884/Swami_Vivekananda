तुम्हें कोई पढ़ा नहीं सकता, कोई आध्यात्मिक नहीं बना सकता। तुमको सब कुछ खुद अंदर से सीखना हैं। आत्मा से अच्छा कोई शिक्षक नही हैं।  <br>
English: You have to grow from the inside out. None can teach you, none can make you spiritual. There is no other teacher but your own soul. <br>
– स्वामी विवेकानंद (Swami Vivekananda) <br>
</p>


